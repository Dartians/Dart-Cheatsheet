// Operators | Conditionals

void main() {
  print(5 == 5); // Equal
  print(5 != 5); // Not equal
  print(5 >= 5); // Greater or equal
  print(5 > 5); // Greater than
  print(5 <= 5); // Less or equal
  print(5 < 5); // Less than

  // In line conditions
  int age = 16;
  /// Here, if age is greater than 18 then type is "adult"
  /// else it's "child"
  String type = age > 18 ? "adult" : "child";
}
